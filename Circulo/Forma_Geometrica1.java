
public class Forma_Geometrica1 {
	int raio;
	double pi;
	String cor;
	
	public Forma_Geometrica1(int raio, double pi, String cor) {
		super();
		this.raio = raio;
		this.pi = pi;
		this.cor = cor;
	}

	public int getRaio() {
		return raio;
	}

	public void setRaio(int raio) {
		this.raio = raio;
	}

	public double getPi() {
		return pi;
	}

	public void setPi(double pi) {
		this.pi = pi;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}
	
	

	
	
	
	
	

}
